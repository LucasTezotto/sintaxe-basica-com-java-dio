public class BoletimEstudantil {
    public static void main(String[] args) {
        int media = 7;

        if (media >= 7){
            System.out.println("Aprovado");

        } else if (media <= 7) {
            System.out.println("Reprovado");

        } else {
            System.out.println("Recuperação");
        }
    }
}
